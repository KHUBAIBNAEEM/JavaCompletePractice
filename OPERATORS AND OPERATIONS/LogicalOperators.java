// Demonstrate logical operators with a truth table

public class LogicalOperators {
    public static void main(String[] args) {
        boolean a = true, b = false;
        System.out.println("a && b: " + (a && b)); // LOGICAL AND
        System.out.println("a || b: " + (a || b)); // LOGICAL OR
        System.out.println("!a: " + (!a)); // LOGICAL NOT
    }
}

// Feature	Logical Operators
// Operand Type:	Boolean
// Operation:	Logical AND, OR, NOT
// Evaluation:	Short-circuit
// Primary Use:	Decision-making
