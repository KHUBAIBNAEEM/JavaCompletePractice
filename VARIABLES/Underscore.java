//  Demonstrate the use of underscores in numeric literals

public class Underscore {
public static void main(String[] args) {
    int million = 10_00_00_0; //underscore help us to determine the number
    double gravity = 9.8_888_888_888;
    System.out.println("million " + million);
    System.out.println("gravity " + gravity);
}
    
}
