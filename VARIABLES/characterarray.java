public class characterarray {
    public static void main(String[] args) {
       char []arr = {'K', 'H', 'U', 'B', 'A', 'I', 'B'};
       System.out.println(arr); // PRINTS HORIZONTICALLY
       for (char c:arr){ //Enhance Loop for printing VERTICALLY
           System.out.println(c);
       }
    }
   }