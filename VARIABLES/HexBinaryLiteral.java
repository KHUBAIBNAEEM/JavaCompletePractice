//Assign and print hexadecimal and binary literals    
    class HexBinaryLiterals {
        public static void main(String[] args) {
            int hex = 0x2BAF; // Hexadecimal literal
            int bin = 0b101010; // Binary literal
            System.out.println("Hexadecimal: " + hex);
            System.out.println("Binary: " + bin);
        }
    }

